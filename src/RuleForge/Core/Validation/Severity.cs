namespace RuleForge.Core.Validation
{
    /// <summary>
    /// Represents the severity of a validation error.
    /// </summary>
    public enum Severity
    {
        /// <summary>
        /// Information level severity.
        /// </summary>
        Info,

        /// <summary>
        /// Warning level severity.
        /// </summary>
        Warning,

        /// <summary>
        /// Error level severity.
        /// </summary>
        Error
    }
}